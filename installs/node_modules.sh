#!/usr/bin/env bash

source '../utils.sh'

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

declare -a NPM_PACKAGES=(
  'gulp'
  'bower'
  'svgo'
  'jshint'
)

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

main() {
  # Check if `npm` is installed
  if ! cmd_exists 'npm'; then
    print_error 'npm is required, please install it!\n'
    exit 1
  fi

  # Install the `npm` packages
  for i in ${NPM_PACKAGES[@]}; do
    execute "npm install --global $i" "$i"
  done
}

main
